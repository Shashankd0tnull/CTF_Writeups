# Predefined decimal values from the DATA statements
data_values = [0xD3, 0xD4, 0xC1, 0xCE, 0xC4, 0xC1, 0xD2, 0xC4, 0xDF, 0xC3,
               0xCF, 0xC4, 0xC5, 0xDF, 0xC6, 0xCF, 0xD2, 0xDF, 0xC9, 0xCE,
               0xC6, 0xCF, 0xD2, 0xCD, 0xC1, 0xD4, 0xC9, 0xCF, 0xCE, 0xDF,
               0xC9, 0xCE, 0xD4, 0xC5, 0xD2, 0xC3, 0xC8, 0xC1, 0xCE, 0xC7,
               0xC5]

# Initialize flag string
flag = ""

# Hexadecimal Conversion
for value in data_values:
    hex_char = hex(value - 128)[2:]
    ascii_char = chr(int(hex_char, 16))  # Convert hex to ASCII
    flag += ascii_char

# Print the flag
print("FLAG{"+flag+"}")